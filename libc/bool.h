#ifndef BOOL_H
#define BOOL_H

/**
 * Boolean type.
 *
 * Represents a 1 bit value that is either
 * `1` or `0` - `true` or `false`
 */
typedef enum _bool {
    /**
     * A true value represented by `1`
     */
    TRUE = 1,
    /**
     * A false value represented by `0`
     */
    FALSE = 0
} BOOL;

#endif // BOOL_H
